var first = 5;
var second = 4;
console.log("La prima variabile è pari a " + first + " la seconda è pari a " + second);
// 1) Somma
console.log("Somma: " + (first + second));
// 2) Prodotto
console.log("Prodotto: " + (first * second));
// 3) Potenza
console.log("Potenza: " + (Math.pow(first, second)));
// 4) Quoziente
console.log("Quoziente: " + (first / second));
// 4) Resto
console.log("Resto: " + (first % second));